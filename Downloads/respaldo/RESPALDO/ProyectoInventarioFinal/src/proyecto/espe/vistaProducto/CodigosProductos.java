    private void consultarDatos() {
        DefaultTableModel modeloTabla = (DefaultTableModel) tbl_Inventario.getModel();
        modeloTabla.setRowCount(0);
        List<Inventario> listaInventario = InventarioServicio.ListaInventario();

        for (Inventario inventario : listaInventario) {
            modeloTabla.addRow(new Object[]{inventario.getCodigo(), inventario.getProducto()});
        }
    }
    
