    private void btEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEliminarActionPerformed
        int fila = tbl_Inventario.getSelectedRow();
        if (fila != -1) {
            InventarioServicio.EliminarInventario((Integer) tbl_Inventario.getValueAt(fila,0));
            consultarDatos();
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione la fila que desea Eliminar");
        }
    }//GEN-LAST:event_btEliminarActionPerformed

    private void btAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAgregarActionPerformed
        Inventario inventario = new Inventario(Integer.parseInt(txtCodigo.getText()), txtProducto.getText());
        
        if(InventarioServicio.InsertarInventario(inventario)){
            JOptionPane.showMessageDialog(null,"Datos Ingresados correctamente");
            limpiarDatos();
            consultarDatos();
        }
        else{
            JOptionPane.showMessageDialog(null,"no se pudo agregar los datos");
        }
    }//GEN-LAST:event_btAgregarActionPerformed

