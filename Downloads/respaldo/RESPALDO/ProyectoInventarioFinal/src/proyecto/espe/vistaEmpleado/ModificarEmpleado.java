    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (!txt_cedula.getText().trim().equals("")
                && !txt_nombre.getText().trim().equals("")
                && !txt_apellido.getText().trim().equals("")
                && !txt_nombre.getText().trim().equals("")
                && !txt_telefono.getText().trim().equals("")
                && !txtCargo.getText().trim().equals("")
                && !txt_correo.getText().trim().equals("")
                && !txt_usuario.getText().trim().equals("")
                && !txt_direccion.getText().trim().equals("")
                && !cmb_perfil.getSelectedItem().equals("--Seleccione")
                && JOptionPane.showConfirmDialog(this, "seguro que desea guardar datos?", "Ingreso Cliente",
                        JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE) == 0) {
            String dato = cmb_perfil.getSelectedItem().toString();
            String[] perfil = dato.split("-");
            id_Perfil = Integer.parseInt(perfil[0].trim());

            Empleado empleado = new Empleado(
                    txt_cedula.getText(),
                    txt_nombre.getText(),
                    txt_apellido.getText(),
                    txtCargo.getText(),
                    txt_correo.getText(),
                    txt_telefono.getText(),
                    txt_direccion.getText(),
                    txt_usuario.getText(),
                    txtContraseña.getText(),
                    id_Perfil
            );
            if (EmpleadoServicio.ActualizarEmpleado(empleado)) {
                validarPerfil();
                JOptionPane.showMessageDialog(null, "Datos actualizados correctamente");
            } else {
                JOptionPane.showMessageDialog(null, "No fue posible actualizar los datos");
            }

        }
    }//GEN-LAST:event_btnActualizarActionPerformed

