    public void cargarComboPerfil(int idPerfil) {
        for (Perfil rol : listaPerfiles) {
            if (idPerfil != rol.getId_perfil()) {
                cmb_perfil.addItem(rol.getId_perfil() + "-" + rol.getNombrePerfil());
            }
        }
    }

    public void cargarDatos() {
        empleado = new EmpleadoServicio().BuscarCedulaEmpleado(cedula);
        txt_cedula.setText(empleado.getCedula());
        cmb_perfil.addItem(empleado.getIdPerfil() + "-" + empleado.getNombrePerfil());
        txt_nombre.setText(empleado.getNombre());
        txt_apellido.setText(empleado.getApellido());
        txtCargo.setText(empleado.getCargo());
        txt_telefono.setText(empleado.getTelefono());
        txt_correo.setText(empleado.getEmail());
        txt_usuario.setText(empleado.getUsuario());
        txt_direccion.setText(empleado.getDireccion());
        cargarComboPerfil(empleado.getIdPerfil());
        txtContraseña.setText(empleado.getClave());
    }

